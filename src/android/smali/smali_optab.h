static inline const smali_op_t *find_smali_op(const char *name) {
    for (size_t i = 0; i < sizeof(smali_optab)/sizeof(smali_optab[0]); i++) {
        if (strcmp(smali_optab[i].name, name) == 0) return &smali_optab[i];
    }
    return NULL;
}

